// pair_hash.h
#ifndef PAIR_HASH_H
#define PAIR_HASH_H

#include <functional>
//unordered_set
template <typename T1, typename T2>
struct pair_hash {
    size_t operator()(const std::pair<T1, T2>& p) const {
        auto h1 = std::hash<T1>{}(p.first);
        auto h2 = std::hash<T2>{}(p.second);
        return h1 ^ (h2 << 1);
    }
};

#endif
